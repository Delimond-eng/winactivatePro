// Load service data when page loads
document.addEventListener("DOMContentLoaded", function () {
  // Get service from URL parameter or default to windows-activation
  const urlParams = new URLSearchParams(window.location.search);
  const serviceKey = urlParams.get("service") || "windows-activation";

  loadService(serviceKey);

  // Initialize other functionality
  initializeMobileMenu();
  initializeContactForm();
  initializeWhatsApp();
});

// Function to load service data
function loadService(serviceKey) {
  const service = serviceData[serviceKey];
  if (!service) return;

  // Update URL without page reload
  const newUrl = new URL(window.location);
  newUrl.searchParams.set("service", serviceKey);
  window.history.pushState({}, "", newUrl);

  // Update page content
  document.getElementById("service-icon").className = service.icon;
  document.getElementById("service-title").textContent = service.title;
  document.getElementById("service-subtitle").textContent = service.subtitle;
  document.getElementById("service-price").textContent = service.price;
  document.getElementById("service-duration").textContent = service.duration;
  document.getElementById("service-warranty").textContent = service.warranty;
  document.getElementById("service-description").innerHTML =
    service.description;

  // Update page title
  document.title = `${service.title} - WinActivate Pro`;

  // Pre-select service in contact form
  const serviceSelect = document.getElementById("service");
  if (serviceSelect) {
    serviceSelect.value = serviceKey;
  }

  // Scroll to top
  window.scrollTo({ top: 0, behavior: "smooth" });
}

// Mobile menu functionality
function initializeMobileMenu() {
  const mobileMenu = document.getElementById("mobile-menu");
  const navMenu = document.querySelector(".nav-menu");

  if (mobileMenu && navMenu) {
    mobileMenu.addEventListener("click", () => {
      mobileMenu.classList.toggle("active");
      navMenu.classList.toggle("active");
    });

    // Close mobile menu when clicking on a link
    document.querySelectorAll(".nav-menu a").forEach((link) => {
      link.addEventListener("click", () => {
        if (window.innerWidth <= 768) {
          mobileMenu.classList.remove("active");
          navMenu.classList.remove("active");
        }
      });
    });
  }
}

// Contact form functionality
function initializeContactForm() {
  const contactForm = document.getElementById("contactForm");
  if (!contactForm) return;

  contactForm.addEventListener("submit", function (e) {
    e.preventDefault();

    // Get form data
    const formData = new FormData(contactForm);
    const data = Object.fromEntries(formData);

    // Basic validation
    if (!data.name || !data.email || !data.service) {
      alert("Please fill in all required fields.");
      return;
    }

    // Email validation
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(data.email)) {
      alert("Please enter a valid email address.");
      return;
    }

    // Simulate form submission
    const submitButton = contactForm.querySelector('button[type="submit"]');
    const originalText = submitButton.textContent;

    submitButton.textContent = "Sending...";
    submitButton.disabled = true;

    setTimeout(() => {
      alert(
        "Thank you for your message! We will get back to you within 1 hour."
      );
      contactForm.reset();
      submitButton.textContent = originalText;
      submitButton.disabled = false;
    }, 1500);
  });
}

// WhatsApp functionality
function initializeWhatsApp() {
  document.querySelectorAll(".whatsapp-link").forEach((link) => {
    link.addEventListener("click", (e) => {
      e.preventDefault();
      openWhatsApp();
    });
  });
}

function openWhatsApp() {
  const phoneNumber = "15551234567";
  const currentService = document.getElementById("service-title").textContent;
  const message = `Hello! I would like to inquire about your ${currentService} service.`;
  const url = `https://wa.me/${phoneNumber}?text=${encodeURIComponent(
    message
  )}`;
  window.open(url, "_blank");
}

// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", function (e) {
    e.preventDefault();
    const target = document.querySelector(this.getAttribute("href"));
    if (target) {
      target.scrollIntoView({
        behavior: "smooth",
        block: "start",
      });
    }
  });
});

// Add click to call functionality
document.addEventListener("DOMContentLoaded", () => {
  const phoneNumbers = document.querySelectorAll(".contact-details p");
  phoneNumbers.forEach((phone) => {
    if (phone.textContent.includes("+1")) {
      phone.style.cursor = "pointer";
      phone.addEventListener("click", () => {
        window.location.href = `tel:${phone.textContent}`;
      });
    }
  });
});
